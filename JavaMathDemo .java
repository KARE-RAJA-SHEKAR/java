import java.lang.*;
public class JavaMathDemo {
    public static void main(String args[]) {
        double x = 48;
        double y = 4;
        System.out.println("Maximum of x and y is " +Math.max(x,y));
        System.out.println("Minimum of x and y is " +Math.min(x,y));
        System.out.println("power of x to y is " +Math.pow(x,y));
        System.out.println("Log10(x) is " +Math.log10(x));
        System.out.println("Round value of 4.56 is "+Math.round(4.56));
    }
}
